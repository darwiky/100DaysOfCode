# This is a program that asks the user for the name of the city they grew up in and the name of their
# pet. It then prints out a band name using the city and pet name.
print("Welcome to the Band Name Generator.")
street = str(input("What's the name of the city you grew up in?\n"))
pet = str(input("What's your pet's name?\n"))
print(f"Your band name could be {street} {pet}")
